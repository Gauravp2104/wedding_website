// Dropdown order: priority countries first (in the order listed),
// a divider, then everything else alphabetically.
export function orderedCountries() {
  const byCode = Object.fromEntries(countries.map((c) => [c.code, c]));
  const top = priorityCodes.map((c) => byCode[c]).filter(Boolean);
  const rest = countries.filter((c) => !priorityCodes.includes(c.code));
  return { top, rest };
}
